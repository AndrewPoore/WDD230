const fruitURL = 'https://brotherblazzard.github.io/canvas-content/fruit.json';

async function fruitFetch() {
    try {
        const fruitResponse = await fetch(fruitURL);
        if (fruitResponse.ok) {
            const fruitData = await fruitResponse.json();
            console.log(fruitData);
            fruitResults(fruitData);
        } else {
            console.log(`Response not OK ${await fruitResponse.text()}`);
        }
    } catch (fruitError) {
        console.log(`Error: ${fruitError.message}`);
    }
}

